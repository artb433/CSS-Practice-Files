function switchCase(){
    var x=0;
    switch(x)
    {
        case 0:
            document.write("zero");
        case 1:
            document.write("one");
        break;
        case 2:
            document.write("two");
        break;
        default:
            document.write("Enter numbers 0 to 2");
        break;
    }
}
switchCase();